    @staticmethod
    def load_string(offset: int) -> str:
        cad = ""
        while True:
            byte = Rars.load_byte(offset)
            if byte == 0:
                break
            # print(f"Offset: {offset:x}, Byte: {byte:x}")
            cad = cad + chr(byte)
            offset = offset + 1

        return cad

