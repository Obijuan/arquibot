# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# ‚îÄ‚îÄ MODULO RARS
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
class Rars:

    # ‚îÄ‚îÄ‚îÄ Anchura de las lineas del encabezado
    WIDTH = 40

    # ‚îÄ‚îÄ Nombre del ejecutable del Rars
    NAME = "rars1_6.jar"

    # ‚îÄ‚îÄ URL de descarga del RARs
    URL = "https://github.com/TheThirdOne/rars/releases/download/v1.6/"\
          f"{NAME}"

    # ‚îÄ‚îÄ Numero maximo de ciclos a probar
    MAX_STEPS = 10000

    # ‚îÄ‚îÄ Nombre de un fichero a incluir
    INCLUDE_ASM = "asm/so.s"

    # Tipo enumerado para indicar el tipo de bonus
    BONUS_INSTRUCCIONES = 0
    BONUS_CICLOS = 1

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ SEGMENTO DE CODIGO
    # ‚îÄ‚îÄ Fichero donde volcar el segmento de c√≥digo
    TEXT = "text.hex"

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ SEGMENTO DE DATOS
    # ‚îÄ‚îÄ Fichero donde volcar el segmento de datos
    DATA = "data.hex"

    # ‚îÄ‚îÄ Numero de bytes a volcar del segmento de datos
    DATA_SIZE = 256

    # ‚îÄ‚îÄ Guardar las salidas del rars y del programa
    # ‚îÄ‚îÄ Al ejecutar el Rars
    stderr = ""
    stdout = ""

    # ‚îÄ‚îÄ Texto enviado por la entrada estandar
    input = ""

    # ‚îÄ‚îÄ ciclos
    ciclos = 0

    # ‚îÄ‚îÄ Registros
    regs = []

    # ‚îÄ‚îÄ Numero de instrucciones
    instrucciones = 0

    # ‚îÄ‚îÄ Variables
    variables = []

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # ‚îÄ‚îÄ CONSTRUCTOR
    # ‚îÄ‚îÄ Entradas:
    # ‚îÄ‚îÄ  * main: Nombre del fichero ensamblador principal
    # ‚îÄ‚îÄ  * include: Nombre del fichero a incluir
    # ‚îÄ‚îÄ  * expected_data: Indicar si el programa debe tener segmento de datos
    # ‚îÄ‚îÄ  * input: Texto a enviar por la entrada estandar
    # ‚îÄ‚îÄ  * tipo_bonus: Tipo de bonus
    # ‚îÄ‚îÄ    * Rars.BONUS_INSTRUCCIONES
    # ‚îÄ‚îÄ    * Rars.BONUS_CILOS
    # ‚îÄ‚îÄ  * bonus: valor maximo para conseguir los bonus
    # ‚îÄ‚îÄ     (instrucciones o ciclos)
    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    def __init__(self,
                 main: str,
                 include: str = "",
                 expected_data: bool = False,
                 input: str = "",
                 tipo_bonus: int = BONUS_INSTRUCCIONES,
                 bonus: int = 0):

        # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ Guardar los parametros pasados

        # ‚îÄ‚îÄ Nombre del fichero MAIN a ensamblar
        self.main_asm = main
        Rars.INCLUDE_ASM = include
        self.expected_data = expected_data
        Rars.input = input
        Rars.tipo_bonus = tipo_bonus
        Rars.bonus = bonus

        # ‚îÄ‚îÄ Estado del test
        self.ok = False

        # ‚îÄ‚îÄ Indica si se han producido errores
        self.errors = False

        # ‚îÄ‚îÄ El programa analizado tiene segmento de Codigo
        self.has_text = False

        # ‚îÄ‚îÄ El programa analizado tiene segmento de datos
        self.has_data = False

        # ‚îÄ‚îÄ Mostrar el encabezado
        Rars.show_header()

        # -- Comprobar si el rars existe
        # -- Si no es asi se descarga
        Rars.check()

        # -- Borrar los archivos temporales generados
        # -- en ejecuciones anteriores
        Rars.delete_data()
        Rars.delete_text()

        # --- Comprobar si el fichero asm existe
        ok = self.check_main_asm()
        if not ok:
            return

        # --- Comprobar si el fichero a incluir existe
        Rars.check_include_asm()

        # -- Ejecutar el Rars!
        self.exec()

        # -- Comprobar si es un error de ensamblado
        ok = self.check_asm_errors()
        if not ok:
            return

        # -- Comprobar si se ha generado el fichero con el volcado de memoria
        # -- si no se ha generado es porque no se ha declaro el segmento
        # --- de datos
        self.check_data()

        # --- Comprobar si se ha generado el segmento de codigo
        ok = self.check_text()
        if not ok:
            return

        # -- Comprobar si hay runtime error
        ok = self.check_runtime_error()
        if not ok:
            return

        # ---- Leer la salida del Rars para obtener los registros y los ciclos
        # ---- Actualiza Rars.ciclos y Rars.regs
        Rars.process_output()

        # -- Analizar el segmento de codigo
        Rars.process_code()

        # -- Comprobar como se ha realizado la salida del programa
        Rars.check_exit()

        # -- Leer todas las variables del segmento de datos
        Rars.read_variables()

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # ‚îÄ‚îÄ Imprimir el encabezado de ARQUI-BOTS
    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
