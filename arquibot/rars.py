            cmd,
            text=True,         # -- Entrada y salida son cadenas de texto
            input=Rars.input,  # -- Cadena para la entrada estandar
            stdout=subprocess.PIPE,  # -- Salida estandar
            stderr=subprocess.PIPE   # -- Salida de error
