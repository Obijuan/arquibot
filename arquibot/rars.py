    @staticmethod
    def load_byte(off: int) -> int:

        # -- Obtener direccion de palabra
        dir_word = off >> 2

        # -- Obtener el numero de byte dentro de la palabra
        nbyte = off & 0x3

        # -- Leer la palabra
        word = Rars.variables[dir_word]

        # -- Obtener el byte
        byte = (word >> (nbyte * 8)) & 0xFF

        # -- Devolver el byte
        return byte

